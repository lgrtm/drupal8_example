namespace Drupal\example\Calculator;


class Calculator {

	function add($arg1, $arg2) {
		return $arg1 + $arg2;
	}

	function subtract($arg1, $arg2) {
		return $arg1 - $arg2;
	}

	function multiplication($arg1, $arg2) {
		return $arg1 * $arg2;
	}

	function division($arg1, $arg2) {
		return $arg1 / $arg2;
	}

}